import { cn } from '../../lib/utils'
import { sectionHasContent } from '../../types/json-resume'

import type { JSONResume, ResumeSectionId } from '../../types/json-resume'
import { Card } from '../ui/card'

interface SectionNavProps {
  sections: readonly {
    id: ResumeSectionId
    label: string
    icon: string
  }[]
  activeSection: ResumeSectionId
  onSectionChange: (section: ResumeSectionId) => void
  resumeData: JSONResume
}

export function SectionNav({
  sections,
  activeSection,
  onSectionChange,
  resumeData,
}: SectionNavProps) {
  return (
    <Card variant="default" className="p-4">
      <h2 className="text-xs font-semibold text-mid-gray uppercase tracking-wider mb-4">
        Sections
      </h2>
      <ul className="space-y-1">
        {sections.map((section) => {
          const hasContent = sectionHasContent(resumeData, section.id)
          const isActive = activeSection === section.id

          return (
            <li key={section.id}>
              <button
                onClick={() => onSectionChange(section.id)}
                className={cn(
                  'w-full flex items-center px-3 py-2.5 text-sm font-medium rounded-lg transition-all duration-200',
                  isActive
                    ? 'bg-amber/10 text-amber-dark'
                    : 'text-charcoal hover:bg-charcoal/5 hover:text-amber'
                )}
              >
                <SectionIcon name={section.icon} className="mr-3 h-5 w-5" />
                <span className="flex-1 text-left">{section.label}</span>
                {hasContent && (
                  <span
                    className={cn(
                      'ml-2 h-2 w-2 rounded-full',
                      isActive ? 'bg-amber' : 'bg-success'
                    )}
                  />
                )}
              </button>
            </li>
          )
        })}
      </ul>
    </Card>
  )
}

